using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace InnovateQ.Assignment.Core.Entities
{
    public class User
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the identifier of the user. </summary>
        ///
        /// <value> The identifier of the user. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public int UserId { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the name. </summary>
        ///
        /// <value> The name. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public string Name { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the designation. </summary>
        ///
        /// <value> The designation. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public string Designation { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the joining date. </summary>
        ///
        /// <value> The joining date. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public DateTime JoiningDate { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the country. </summary>
        ///
        /// <value> The country. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public string Country { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the state. </summary>
        ///
        /// <value> The state. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public string State { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the street. </summary>
        ///
        /// <value> The street. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public string? Street { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the pin code. </summary>
        ///
        /// <value> The pin code. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public string? PinCode { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the full pathname of the image file. </summary>
        ///
        /// <value> The full pathname of the image file. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public string? ImagePath { get; set; }

    }
}
